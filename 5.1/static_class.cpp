#include<iostream>
using namespace std;

class Arithmatic
{

     public :


int arithmatic(int a1 , int a2 )
{
     cout << "division : " << a1 / a2 <<endl;    
}

int arithmatic(int a1 , int a2 , int a3 )
{
     cout << "subtraction : " << a1 - a2 - a3 <<endl;
}

int arithmatic(int a1 , int a2 , int a3 , int a4 )
{
     cout << "multiplication : " << a1 * a2 * a3 * a4 <<endl;

}
int arithmatic(int a1 , int a2 , int a3 , int a4 , int a5)
{
      
      cout << "addition : " << a1 + a2 + a3 + a4 + a5 <<endl;
}
     
};
